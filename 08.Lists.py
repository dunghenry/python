mylist = ["apple", "orange", "mango"]

print(mylist)  # ['apple', 'orange', 'mango']
print(type(mylist))  # <class 'list'>
print(len(mylist))  # 3

data = list(("Apple", "Orange", "Mango"))
print(data)  # ['Apple', 'Orange', 'Mango']
print(data[0])  # Apple

print(data[-1])  # Mango

list = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]

print(list[2:5])    # ['cherry', 'orange', 'kiwi']
print(list[:4])     # ['apple', 'banana', 'cherry', 'orange']

print("apple" in list)  # True

