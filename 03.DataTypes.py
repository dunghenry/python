# list
l = ["PHP", "Go", "Python"]
print(l)  # ['PHP', 'Go', 'Python']
print(type(l))  # list
print(l.__len__())  # 3

# tuple

t = ("apple", "banana", "cherry")

print(t[1])  # banana

# range
r = range(6)

print(r)  # range(0, 6)


# dict
d = {"name": "Dung", "age": 22}

print(d['name'])  # Dung


# set
s = {"apple", "banana", "cherry"}
print(s)


# bool

b = True

print(b)  # True

# none
n = None
print(n)  # None
